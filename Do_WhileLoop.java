public class Do_WhileLoop {
    public static void main(String []args){
        int i = 1;
        do{
            System.out.println(i);
        i++;
        }while(i<=5);
        
    }

    public static void main2(){
        do{
            System.out.println("Infinitive do while loop");
        }while(true);
    }
}

